/*
 *  将阿拉伯数字 0 ~ 9 转换成中文数字 零 ~ 九
 * 
 * */
String.prototype.convertToCH = function(){
	var arr = this.split("");
	for( var i = 0, len = arr.length;i < len;i++ ){
		if( !(/\d/.test(arr[i])) ){
			continue;
		}
		switch(arr[i]){
			case "0": arr[i] = "零";break;
			case "1": arr[i] = "一";break;
			case "2": arr[i] = "二";break;
			case "3": arr[i] = "三";break;
			case "4": arr[i] = "四";break;
			case "5": arr[i] = "五";break;
			case "6": arr[i] = "六";break;
			case "7": arr[i] = "七";break;
			case "8": arr[i] = "八";break;
			case "9": arr[i] = "九";break;
		}
	}
	return arr.join("");
}

Number.prototype.convertToCH = function(){
	return String(this).convertToCH();
}